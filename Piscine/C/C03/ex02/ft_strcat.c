char	*ft_strcat(char *dest, char *src)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	while (dest[a] != '\0')
	{
		a++;
	}
	while ((dest[a] = src[b]) != '\0')
	{
		a++;
		b++;
	}
	return (dest);
}
