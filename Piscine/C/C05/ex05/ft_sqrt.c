int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 2;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (sqrt * sqrt <= nb && sqrt < 46341)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
